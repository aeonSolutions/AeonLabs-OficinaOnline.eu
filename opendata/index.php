<h2>Sobre o projecto</h2>
<p>A ideia OficinaOnline  surgiu durante a fase de prova de mercado para um projecto empreendedorismo onde ficou evidente a ausência de uma maior confiança nas transacções de peças e componentes para automóveis.</p> 

<p>Ao contribuir para este projecto está a construir uma plataforma web para o comércio justo de automóveis usados, peças e componentes auto em Open Source e Open Data. Para quem não sabe o que significam os conceitos anteriores, "Open Source" e "Open Data" pense em grátis ...de borla ... e em código disponível públicamente para qualquer pessoa poder ver, consultar e utilizar...</p>
O projecto não visa o lucro mas a disponibilização de um serviço de forma práticamente gratuita!</p>

<p>Este projecto vai estar disponível em www.oficinaonline.eu e depois de pronto permite que tanto o vendedor como o cliente possam efectuar uma transacção segura e sem receio do que está a comprar ou a vender.</p>

<p>Para isso está a ser implementado um sistema de autenticação sem password que utiliza o número do telemovél para poder utilizar e efectuar compras no dia-a-dia...
A implementação de verificações de segurança como por exemplo:</p>
<ul>
<li>A possibilidade de inserir o número de registo de transporte da encomenda após a compra;</li>
<li>Deixar uma classificação e comentar sobre o vendedor e sobre o comprador;</li>
<li>Mostrar dados estatísticos sobre a evolução de preços de cada peça, componente ou automóvel que ande á procura;</li>
<li>Ter a sua loja online e publicitar de forma gratuita na internet e nas redes sociais;</li>
<li>Cada transacção efectuada com sucesso terá um custo máximo de 1 cêntimo... a colocação e anúncio de venda será sempre grátis;</li>
<li>Contribua para este projecto...ajude-o a que seja real mais rápidamente !!!</li>
</ul>
<p>Não estão previstas parcerias comerciais empresarias clássicas ou tradicionais. O autor está aberto à colaboração no desenvolvimento do código em formato Open Source e Open Data.</p>
